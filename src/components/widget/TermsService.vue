제 1 조 (목적)<br>
이 이용약관(이하 “약관”이라한다)은 경찰청이 주관하는 연구개발 사업 “경찰관 맞춤형 건강관리 서비스를 위한 지능형 빅데이터 통합플랫폼 개발”(이하 “사업단”이라한다)의 경찰관 건강관리 통합 플랫폼(이하 “폴라이프”라한다)에서 제공하는 서비스를 이용함에 있어서 플랫폼과 이용자의 권리, 의무 및 책임사항을 규정함을 목적으로 한다.<br>
<br>
제 2 조 (용어의 정의)<br>
① “플랫폼”이란 사업단이 데이터, UI, API 등을(이하 “데이터 등”이라한다)을 이용자에게 제공하기 위하여, 데이터를 수집, 처리, 저장, 분석 및 시각화 등을 통하여 회원에게 혜택을 제공하는 시스템을 말하며, 아울러 플랫폼을 운영하는 사업단의 의미로도 사용한다.<br>
② “이용자”란 “플랫폼”에 접속하여 이 약관에 따라 “플랫폼”이 제공하는 서비스를 받는 회원 및 비회원을 말한다.<br>
③ “회원”이라 함은 “플랫폼”에 회원등록을 한 자로서, 계속적으로 “플랫폼”이 제공하는 서비스를 이용할 수 있는 자를 말한다.<br>
④ “비회원”이라 함은 회원에 가입하지 않고 “플랫폼”이 제공하는 서비스를 이용하는 자를 말한다.<br>
⑤ “아이디(ID)”라 함은 “회원”의 식별과 서비스 이용을 위하여 “회원”이 정하고 “플랫폼”이 승인하는 문자 또는 숫자의 조합을 말한다.<br>
⑥ “비밀번호(PASSWORD)”라 함은 “회원”이 부여받은 “아이디”와 일치되는 “회원”임을 확인하고 비밀보호를 위해 “회원” 자신이 정한 문자 또는 숫자의 조합을 말한다.<br>
<br>
제 3 조 (약관 등의 명시와 설명 및 개정)<br>
① “플랫폼”은 이 약관의 내용을 “이용자”가 쉽게 알 수 있도록 “플랫폼”의 회원가입 화면에 공시한다. 다만, 약관의 내용은 “이용자”가 연결화면을 통하여 볼 수 있도록 할 수 있다.<br>
② 이 약관은 “회원”의 동의와 “플랫폼”의 승낙으로 효력이 발생합니다. “플랫폼”은 관련 법령에 위배되지 않는 범위 안에서 개정할 수 있습니다. 약관이 개정될 시 서비스를 통해 공지함으로써 효력을 유지합니다.<br>
③ “플랫폼”은 「약관의 규제에 관한 법률」, 「전자문서 및 전자거래기본법」, 「전자서명법」, 「정보통신망 이용촉진 및 정보보호 등에 관한 법률」 등 관련 법을 위배하지 않는 범위에서 이 약관을 개정할 수 있다.<br>
④ “플랫폼”이 약관을 개정할 경우에는 적용일자 및 개정사유를 명시하여 현행약관과 함께 플랫폼 초기화면에 그 적용일자 7일 이전부터 적용일자 전일까지 공지한다. 다만, “이용자”에게 불리하게 약관내용을 변경하는 경우에는 최소한 30일 이상의 사전 유예기간을 두고 공지한다. 이 경우 "플랫폼“은 개정 전 내용과 개정 후 내용을 명확하게 비교하여 “이용자”가 알기 쉽도록 표시한다.<br>
⑤ “플랫폼”이 약관을 개정할 경우에는 그 개정약관은 그 적용일자 이후에 체결되는 계약에만 적용되고 그 이전에 이미 체결된 계약에 대해서는 개정 전의 약관을 그대로 적용한다. 다만 이미 계약을 체결한 “이용자”가 개정약관 조항의 적용을 받기를 원하는 뜻을 제3항에 의한 개정약관의 공지기간 내에 “플랫폼”에 송신하여 “플랫폼”의 동의를 받은 경우에는 개정약관 조항을 적용한다.<br>
⑥ “회원”은 정기적으로 서비스를 방문하여 약관의 변경 여부를 확인하여야 합니다. 변경된 약관에 대한 정보를 알지 못해 발생하는 피해는 “플랫폼”에서 책임지지 않습니다. <br>
⑦ “회원”은 변경된 약관에 동의하지 않을 경우 회원탈퇴(해지)를 요청할 수 있습니다. <br>
<br>
제 4 조 (약관 외 준칙)<br>
“플랫폼”은 필요한 경우 서비스 내의 개별항목에 대하여 개별약관 또는 운영원칙(이하 “서비스별 안내”라고한다)를 정할 수 있으며, 이 약관과 “서비스별 안내”의 내용이 상충되는 경우에는 “서비스별 안내”의 내용을 우선하여 적용합니다.<br>
<br>
제 5 조 (서비스의 중단)<br>
① “플랫폼”은 컴퓨터 등 정보통신설비의 보수점검&#183;교체 및 고장, 통신의 두절 등의 사유가 발생한 경우에는 서비스의 제공을 일시적으로 중단할 수 있다.<br>
